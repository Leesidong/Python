# 탈출 문자

# \n : 줄바꿈
print("백문이 불여일견 \n 백견이 불여일타")

# \" : 큰따옴표 출력
print("저는 \"나도 코딩 \"입니다")

# \\ : 문자 내에서 \ 출력
print("C:\\Users\\LSD\\Desktop\\Project Code\\VSCode")

# \r : 커서를 맨 앞으로 이동
# \r뒤에 Pine을 썼기때문에 제일 앞에서 부터 4글자(공백포함) Red 를 지우고 그 자리에 Pine 값을 씀
print("Red Apple \rPine")


# \b : 백스페이스 (한 글자 삭제)
# \b의 앞에 쓴 글자를 지움
print("Redd\bApple")

# \t : 탭
print("Red\tApple")